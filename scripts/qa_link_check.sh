#!/usr/bin/env bash
# qa_link_check.sh — V1-029 release QA: internal link check.
#
# Mechanically verifies (release plan §2.3, §6 "Repository QA": zero broken
# internal normative links) that every relative inline markdown link
# `[text](target)` in the authoritative specification and release-evidence
# surfaces resolves to an existing file. Scope:
#   - the exact normative set (Charter, Vision, 01, 03, 05, 06, 06a, 07, 08,
#     09, 10, 11) and decisions/ADR-*.md;
#   - docs/conventions/*.md;
#   - docs/design/Benchmark_v1.0_Release_Plan.md (non-normative release record);
#   - docs/design/release_evidence/spec-v1.0/*.md.
# External links (http/https/mailto) and link syntax quoted inside inline code
# spans are skipped. Same-file #anchors are skipped; cross-file #fragments are
# validated against the target file's heading/explicit-anchor set. File-level
# resolution and fragment resolution are both enforced for relative links.
#
# Operational tooling only: decides no benchmark meaning (v1-029 proposal §1-§2).
# Evidence: docs/design/release_evidence/spec-v1.0/link_check.txt
# Exit: 0 = pass, 1 = findings, 2 = environment error.
set -u

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)" || exit 2
cd "$REPO_ROOT" || exit 2
EVIDENCE_DIR="docs/design/release_evidence/spec-v1.0"
OUT="$EVIDENCE_DIR/link_check.txt"
[ -d "$EVIDENCE_DIR" ] || { echo "ERROR: evidence directory missing: $EVIDENCE_DIR" >&2; exit 2; }

scan_files=(
  docs/00_Benchmark_Charter.md
  VISION.md
  docs/01_Benchmark_Design.md
  docs/03_Problem_Definition.md
  docs/05_Formal_Definitions.md
  docs/06_Label_Specification.md
  docs/06a_Benchmark_Data_Model.md
  docs/07_Label_Generation_Pipeline.md
  docs/08_Dataset_Specification.md
  docs/09_Baseline_Systems.md
  docs/10_Evaluation_Protocol.md
  docs/11_Benchmark_Assurance.md
  docs/design/Benchmark_v1.0_Release_Plan.md
)
shopt -s nullglob
scan_files+=(decisions/ADR-*.md docs/conventions/*.md "$EVIDENCE_DIR"/*.md)
shopt -u nullglob

findings=()
checked=0

normalize_anchor() {
  printf '%s' "$1" \
    | tr '[:upper:]' '[:lower:]' \
    | sed -E 's/[[:space:]]+/-/g; s/[^a-z0-9\-]//g; s/-+/-/g; s/^-|-$//g'
}

anchor_exists() {
  local file="$1" frag="$2" norm line heading
  norm="$(normalize_anchor "$frag")"
  [ -n "$norm" ] || return 1

  if grep -qiF "id=\"${frag}\"" "$file" || \
     grep -qiF "id='${frag}'" "$file" || \
     grep -qiF "name=\"${frag}\"" "$file" || \
     grep -qiF "name='${frag}'" "$file"; then
    return 0
  fi

  while IFS= read -r line; do
    heading="${line#\#* }"
    [ -n "$heading" ] || continue
    if [ "$(normalize_anchor "$heading")" = "$norm" ]; then
      return 0
    fi
  done < <(grep -E '^#{1,6}[[:space:]]+' "$file")
  return 1
}

for f in "${scan_files[@]}"; do
  [ -f "$f" ] || { findings+=("scan file missing: $f"); continue; }
  dir="$(dirname "$f")"
  # one link target per line
  while IFS= read -r target; do
    case "$target" in
      http://*|https://*|mailto:*) continue ;;
      '#'*) continue ;;
    esac
    path="${target%%#*}"
    frag=""
    [ "$target" != "$path" ] && frag="${target#*#}"
    [ -z "$path" ] && continue
    checked=$((checked + 1))
    if [ "${path#/}" != "$path" ]; then
      findings+=("$f: absolute-path link '$target'")
      continue
    fi
    resolved=""
    if [ -e "$dir/$path" ]; then
      resolved="$dir/$path"
    elif [ -e "$path" ]; then
      resolved="$path"
    fi
    if [ -z "$resolved" ]; then
      findings+=("$f: broken link '$target'")
      continue
    fi
    if [ -n "$frag" ] && ! anchor_exists "$resolved" "$frag"; then
      findings+=("$f: missing anchor '#$frag' in '$target'")
    fi
  done < <(sed 's/`[^`]*`//g' "$f" | grep -oE '\]\([^)]+\)' | sed -E 's/^\]\(//; s/\)$//; s/[[:space:]]+"[^"]*"$//')
done

commit="$(git rev-parse --short HEAD 2>/dev/null || echo 'no-git')"
dirty=""
[ -n "$(git status --porcelain 2>/dev/null)" ] && dirty=" (working tree dirty)"
{
  echo "# link_check.txt — generated by scripts/qa_link_check.sh (V1-029)"
  echo "# Run: $(date -u +%Y-%m-%dT%H:%M:%SZ)  commit: ${commit}${dirty}"
  echo "# Scope: normative set, accepted-ADR register, conventions, release plan, release evidence"
  echo "# Checked: $checked relative link targets across ${#scan_files[@]} files"
  if [ "${#findings[@]}" -eq 0 ]; then
    echo "RESULT: PASS (0 findings)"
  else
    echo "RESULT: FAIL (${#findings[@]} findings)"
    for x in "${findings[@]}"; do echo "FINDING: $x"; done
  fi
} > "$OUT"

echo "qa_link_check: wrote $OUT ($checked targets checked)"
if [ "${#findings[@]}" -eq 0 ]; then
  echo "qa_link_check: PASS"
  exit 0
fi
echo "qa_link_check: FAIL (${#findings[@]} findings)"
sed -n 's/^FINDING: /  - /p' "$OUT"
exit 1
