#!/usr/bin/env bash
# qa_stale_authority_check.sh — V1-029 release QA: stale authority-text check.
#
# Mechanically scans the normative specifications (Charter, Vision, 01, 03, 05,
# 06, 06a, 07, 08, 09, 10, 11) for text that grants or claims stale authority
# (release plan §6 "Repository QA" and the open-question/stale-authority
# controls summarized there: no normative document depends on private
# filesystem paths or review files for authority; no stale absence/status
# claims):
#   1. Private filesystem paths (~/..., /home/..., /Users/..., C:\...).
#   2. References to reviews/ files inside a normative specification (review
#      evidence is input, never authority).
#   3. Stale ADR-status claims: a line asserting an ADR is proposed / pending /
#      reserved / not yet accepted when the decision register shows it Accepted.
#   4. Dangling ADR citations: an ADR-NNN token with no file in decisions/, or
#      whose register file is not Status: Accepted (a Proposed ADR authorizes
#      nothing; decision-records §6).
#
# ADR bodies themselves are NOT scanned: accepted ADRs are append-only as filed
# (decision-records §8) and are not retrofitted by tooling. BM-approved
# non-blocking exceptions (plan §6) are recorded against the evidence file, not
# silently waived here.
#
# Operational tooling only: decides no benchmark meaning (v1-029 proposal §1-§2).
# Evidence: docs/design/release_evidence/spec-v1.0/stale_authority_check.txt
# Exit: 0 = pass, 1 = findings, 2 = environment error.
set -u

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)" || exit 2
cd "$REPO_ROOT" || exit 2
EVIDENCE_DIR="docs/design/release_evidence/spec-v1.0"
OUT="$EVIDENCE_DIR/stale_authority_check.txt"
[ -d "$EVIDENCE_DIR" ] || { echo "ERROR: evidence directory missing: $EVIDENCE_DIR" >&2; exit 2; }

NORMATIVE_DOCS=(
  docs/00_Benchmark_Charter.md
  VISION.md
  docs/01_Benchmark_Design.md
  docs/03_Problem_Definition.md
  docs/05_Formal_Definitions.md
  docs/06_Label_Specification.md
  docs/06a_Benchmark_Data_Model.md
  docs/07_Label_Generation_Pipeline.md
  docs/08_Dataset_Specification.md
  docs/09_Baseline_Systems.md
  docs/10_Evaluation_Protocol.md
  docs/11_Benchmark_Assurance.md
)

findings=()

adr_status() { # adr_status ADR-NNN -> prints front-matter Status of the matching file
  local id="$1" f
  for f in decisions/"$id"-*.md; do
    [ -f "$f" ] || continue
    awk 'NR==1 && $0!="---" {exit} NR>1 && $0=="---" {exit} NR>1 && index($0,"Status:")==1 {v=substr($0,8); gsub(/^[ \t]+|[ \t]+$/,"",v); print v; exit}' "$f"
    return
  done
}

for f in "${NORMATIVE_DOCS[@]}"; do
  [ -f "$f" ] || { findings+=("missing normative file: $f"); continue; }

  # 1. Private filesystem paths.
  while IFS= read -r hit; do
    findings+=("$f:$hit (private filesystem path)")
  done < <(grep -nE '~\/[^[:space:]]+|\/home\/[^[:space:]]+|\/Users\/[^[:space:]]+|[A-Za-z]:\\[^[:space:]]+' "$f" | cut -c1-160)

  # 2. Review files cited inside a normative specification.
  while IFS= read -r hit; do
    findings+=("$f:$hit (reviews/ reference in normative text)")
  done < <(grep -n 'reviews/' "$f" | cut -c1-160)

  # 3. Stale ADR-status claims against the decision register.
  while IFS= read -r hit; do
    lineno="${hit%%:*}"; text="${hit#*:}"
    for id in $(echo "$text" | grep -oE 'ADR-[0-9]{3}' | sort -u); do
      status="$(adr_status "$id")"
      if [ "$status" = "Accepted" ]; then
        findings+=("$f:$lineno: claims $id is proposed/pending/reserved but register shows Accepted: '$(echo "$text" | sed 's/^[[:space:]]*//' | cut -c1-100)'")
      fi
    done
  done < <(grep -niE 'ADR-[0-9]{3}[^0-9].*(proposed|pending|reserved|awaiting|not yet accepted)|(proposed|pending|reserved|awaiting|not yet accepted).*ADR-[0-9]{3}' "$f")

  # 4. Dangling or non-accepted ADR citations.
  for id in $(grep -oE 'ADR-[0-9]{3}' "$f" | sort -u); do
    status="$(adr_status "$id")"
    if [ -z "$status" ]; then
      findings+=("$f: cites $id but decisions/ has no such record (dangling authority)")
    elif [ "$status" != "Accepted" ]; then
      findings+=("$f: cites $id whose register status is '$status' (a non-accepted ADR authorizes nothing)")
    fi
  done
done

commit="$(git rev-parse --short HEAD 2>/dev/null || echo 'no-git')"
dirty=""
[ -n "$(git status --porcelain 2>/dev/null)" ] && dirty=" (working tree dirty)"
{
  echo "# stale_authority_check.txt — generated by scripts/qa_stale_authority_check.sh (V1-029)"
  echo "# Run: $(date -u +%Y-%m-%dT%H:%M:%SZ)  commit: ${commit}${dirty}"
  echo "# Scope: exact normative specification set; ADR bodies excluded (append-only as filed)"
  if [ "${#findings[@]}" -eq 0 ]; then
    echo "RESULT: PASS (0 findings)"
  else
    echo "RESULT: FAIL (${#findings[@]} findings)"
    for x in "${findings[@]}"; do echo "FINDING: $x"; done
  fi
} > "$OUT"

echo "qa_stale_authority_check: wrote $OUT"
if [ "${#findings[@]}" -eq 0 ]; then
  echo "qa_stale_authority_check: PASS"
  exit 0
fi
echo "qa_stale_authority_check: FAIL (${#findings[@]} findings)"
sed -n 's/^FINDING: /  - /p' "$OUT"
exit 1
