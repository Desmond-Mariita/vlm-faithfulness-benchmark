#!/usr/bin/env bash
# qa_terminology_check.sh — V1-029 release QA: terminology check.
#
# Mechanically verifies the ratified terminology dispositions on the normative
# specification set (release plan §6 "Repository QA": terminology check passes
# or has BM-approved non-blocking exceptions):
#   1. ADR-008 (D1 terminology): the canonical D1 display name
#      "Image-independent answer" is present in the Label Specification, and
#      the deprecated historical name "Ungrounded answer" appears in normative
#      text only on lines that mark it deprecated/historical (ADR-008 T5).
#   2. V1-016 (boundary vocabulary): the canonical view names
#      "predictor-visible view", "hidden evaluation view", and "audit view"
#      are used by the Dataset Specification, which owns the release views.
#
# ADR bodies are not scanned (append-only as filed, decision-records §8);
# historical reviews and archives are out of scope by design (ADR-008 §2).
# New terminology dispositions extend this script through their own governed
# work packages; this tool decides no vocabulary itself.
#
# Operational tooling only: decides no benchmark meaning (v1-029 proposal §1-§2).
# Evidence: docs/design/release_evidence/spec-v1.0/terminology_check.txt
# Exit: 0 = pass, 1 = findings, 2 = environment error.
set -u

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)" || exit 2
cd "$REPO_ROOT" || exit 2
EVIDENCE_DIR="docs/design/release_evidence/spec-v1.0"
OUT="$EVIDENCE_DIR/terminology_check.txt"
[ -d "$EVIDENCE_DIR" ] || { echo "ERROR: evidence directory missing: $EVIDENCE_DIR" >&2; exit 2; }

NORMATIVE_DOCS=(
  docs/00_Benchmark_Charter.md
  VISION.md
  docs/01_Benchmark_Design.md
  docs/03_Problem_Definition.md
  docs/05_Formal_Definitions.md
  docs/06_Label_Specification.md
  docs/06a_Benchmark_Data_Model.md
  docs/07_Label_Generation_Pipeline.md
  docs/08_Dataset_Specification.md
  docs/09_Baseline_Systems.md
  docs/10_Evaluation_Protocol.md
  docs/11_Benchmark_Assurance.md
)

LABEL_SPEC="docs/06_Label_Specification.md"
DATASET_SPEC="docs/08_Dataset_Specification.md"

findings=()
info=()

# 1a. Canonical D1 display name present (ADR-008 Alternative C).
if [ -f "$LABEL_SPEC" ]; then
  if grep -q 'Image-independent answer' "$LABEL_SPEC"; then
    info+=("$LABEL_SPEC: canonical D1 display name 'Image-independent answer' present")
  else
    findings+=("$LABEL_SPEC: canonical D1 display name 'Image-independent answer' missing (ADR-008)")
  fi
else
  findings+=("missing normative file: $LABEL_SPEC")
fi

# 1b. Deprecated historical name confined to deprecation notes.
for f in "${NORMATIVE_DOCS[@]}"; do
  [ -f "$f" ] || continue
  while IFS= read -r hit; do
    if ! echo "$hit" | grep -qiE 'deprecated|historical|former'; then
      findings+=("$f: deprecated D1 name 'Ungrounded answer' used outside a deprecation note: '$(echo "$hit" | cut -c1-120)' (ADR-008 T5)")
    fi
  done < <(grep -n 'Ungrounded answer' "$f")
done

# 2. Canonical boundary view vocabulary (V1-016) in the owning specification.
if [ -f "$DATASET_SPEC" ]; then
  for term in 'predictor-visible view' 'hidden evaluation view' 'audit view'; do
    if grep -q "$term" "$DATASET_SPEC"; then
      info+=("$DATASET_SPEC: canonical term '$term' present")
    else
      findings+=("$DATASET_SPEC: canonical boundary term '$term' missing (V1-016)")
    fi
  done
else
  findings+=("missing normative file: $DATASET_SPEC")
fi

commit="$(git rev-parse --short HEAD 2>/dev/null || echo 'no-git')"
dirty=""
[ -n "$(git status --porcelain 2>/dev/null)" ] && dirty=" (working tree dirty)"
{
  echo "# terminology_check.txt — generated by scripts/qa_terminology_check.sh (V1-029)"
  echo "# Run: $(date -u +%Y-%m-%dT%H:%M:%SZ)  commit: ${commit}${dirty}"
  echo "# Rules: ADR-008 D1 display-name disposition; V1-016 canonical boundary vocabulary"
  if [ "${#findings[@]}" -eq 0 ]; then
    echo "RESULT: PASS (0 findings)"
  else
    echo "RESULT: FAIL (${#findings[@]} findings)"
    for x in "${findings[@]}"; do echo "FINDING: $x"; done
  fi
  for x in "${info[@]}"; do echo "INFO: $x"; done
} > "$OUT"

echo "qa_terminology_check: wrote $OUT"
if [ "${#findings[@]}" -eq 0 ]; then
  echo "qa_terminology_check: PASS"
  exit 0
fi
echo "qa_terminology_check: FAIL (${#findings[@]} findings)"
sed -n 's/^FINDING: /  - /p' "$OUT"
exit 1
